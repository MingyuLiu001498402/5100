    private void Displaydriver(Person person){
        
        ImageIcon icon = new ImageIcon(person.getDriverlicense().getPhoto());
        icon.setImage(icon.getImage().getScaledInstance(155, 155, Image.SCALE_DEFAULT));
        photolabel.setIcon(icon);
        
        String bloodtype = person.getDriverlicense().getBloodtype();
        BloodTypeTxt.setText(bloodtype);
        
        String licensenum = person.getDriverlicense().getLicensenum();
        LicenseNumTxt.setText(licensenum);
        
        String pubdate = person.getDriverlicense().getPublicdate();
        PublicDateTxt.setText(pubdate);
        
        String valuedate = person.getDriverlicense().getValuedate();
        ValueDateTxt.setText(valuedate);
        
        
        
        
        
    }

