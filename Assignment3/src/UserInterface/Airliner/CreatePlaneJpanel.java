    private void CreateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateBtnActionPerformed
        // TODO add your handling code here:
        if (Name.getText().isEmpty() || Type.getText().isEmpty() || Seats.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Can not be empty");
        return;
        }
        AirPlane airplane =  airliner.getAirplanedirectory().addAirPlane();
        int seat;        
        try{
            seat = Integer.parseInt(Seats.getText());
                  
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"seats must be a number");    
            return;   
        }
        airplane.setAvailableseat(seat);
        airplane.setModeltype(Type.getText());
        airplane.setName(Name.getText());
        
        JOptionPane.showMessageDialog(null, "Create Plane sucessful");
        Type.setText("");
        Name.setText("");
        Seats.setText("");
    }//GEN-LAST:event_CreateBtnActionPerformed

